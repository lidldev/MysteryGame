func getSingleImage(name string) *ebiten.Image {
	f, err := assets.Open(name)
	if err != nil {
		log.Fatal(err)
	}

	img, _, err := image.Decode(f)
	if err != nil {
		log.Fatal(err)
	}

	return ebiten.NewImageFromImage(img)
}

func mustSubImage(tileSetImage *ebiten.Image, ts tiled.Tileset, id uint32) *ebiten.Image {
	width := ts.TileWidth
	height := ts.TileHeight

	col := int(id) % ts.Columns
	row := int(id) / ts.Columns

	// Plus one because of 1px margin
	if col > 0 {
		width += 1
	}
	if row > 0 {
		height += 1
	}

	sx := col * width
	sy := row * height

	return tileSetImage.SubImage(
		image.Rect(sx, sy, sx+ts.TileWidth, sy+ts.TileHeight),
	).(*ebiten.Image)
}

func getTiled(name string) *ebiten.Image {
	gameMap, err := tiled.LoadFile(name)
	if err != nil {
		fmt.Printf("error parsing map: %s", err.Error())
		os.Exit(2)
	}

	fmt.Println(gameMap)

	renderer, err := render.NewRenderer(gameMap)
	if err != nil {
		fmt.Printf("map unsupported for rendering: %s", err.Error())
		os.Exit(2)
	}

	// Render just layer 0 to the Renderer.
	err = renderer.RenderLayer(0)
	if err != nil {
		fmt.Printf("layer unsupported for rendering: %s", err.Error())
		os.Exit(2)
	}

	// Get a reference to the Renderer's output, an image.NRGBA struct.
	img := renderer.Result

	// Clear the render result after copying the output if separation of
	// layers is desired.

	return ebiten.NewImageFromImage(img)
}
